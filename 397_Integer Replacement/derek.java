class Solution {
    //1234->617->618->309->310->155->156->78->39->40->20->10->5->6->3->4->2->1 (17) not min
    //10000->5000->2500->1250->625->626->313->314->157->158->79->80->40->20->10->5->6->3->4->2->1 (20)
    //10000->5000->2500->1250->625->624->312->156->78->39->38->19->18->9->8->4->2->1 (16)
    public int integerReplacement(int n) {
        int count = 0;
        while (n != 1) {
            if ((n & 1) == 1) {
                n = (((n & 2) == 2) && n != 3) ? n+1 : n-1;
            } else {
                n = n >>> 1;
            }
            count++;
        }
        return count;
    }
}
